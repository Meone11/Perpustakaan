    boolean cekUsername(String User){
        boolean cek = false;
        
        PreparedStatement st;
        ResultSet rs;
        
        String query = "SELECT `user` FROM `Login` WHERE user = ?";
        
        try {
            
            st = (PreparedStatement) MyConncetion.getConnection().prepareStatement(query);               
            st.setString(1, User);
        
            rs = st.executeQuery();
            
            if (rs.next()){
                cek = true;
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RegisForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        return cek;
    }

