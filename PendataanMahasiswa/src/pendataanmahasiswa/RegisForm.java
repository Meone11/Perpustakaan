    private void regisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regisButtonActionPerformed
        // TODO add your handling code here:
        if (rUsername.getText().equalsIgnoreCase("Username") || rPassword.getText().equalsIgnoreCase("Password")){
            JOptionPane.showMessageDialog(null, "Mohon Masukan username atau Password Anda!");
        }else{
            String name = rUsername.getText().trim();
            String pass = rPassword.getText().trim();
            String profile = rProfile.getItemAt(rProfile.getSelectedIndex());
            boolean cek = cekUsername(name);
            
            PreparedStatement ps;
            String query = "INSERT INTO `Login`(`user`, `profile`, `pass`) VALUES (?,?,?)";
            
            try {
                
                if (cek){
                    JOptionPane.showMessageDialog(null, "Username Telah Digunakan!!");
                    rUsername.setText("");
                    rPassword.setText("");
                }else{
                    ps = (PreparedStatement) MyConncetion.getConnection().prepareStatement(query);
                
                    ps.setString(1, name);
                    ps.setString(2, profile);
                    ps.setString(3, pass);

                    if ( ps.executeUpdate() > 0 ){
                        JOptionPane.showMessageDialog(null, "User Telah Ditambahkan");
                        
                        //open form login
                        new LoginForm().setVisible(true);
                        this.dispose();
                    }
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(RegisForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_regisButtonActionPerformed

