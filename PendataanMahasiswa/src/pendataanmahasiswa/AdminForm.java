    boolean validateText(){
        if (nameAdmin.getText().equals("") || ttlAdmin.getDate() == null || npmAdmin.getText().equals("") || prodiAdmin.getText().equals(" ")
                || nomorAdmin.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Data Anda!!");
            return false;
        }else if ( ttlAdmin.getDate().compareTo(new Date()) > 0){
            JOptionPane.showMessageDialog(null, "Tolong isi Tanggal lahir dengan Benar");
            return false;
        }else{
            return true;
        }
    }
    
    

