    private void TableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableDataMouseClicked
        // TODO add your handling code here:
        
        int index = TableData.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TableData.getModel();
        
        nameAdmin.setText(model.getValueAt(index, 0).toString());
        prodiAdmin.setText(model.getValueAt(index, 1).toString());
        nomorAdmin.setText(model.getValueAt(index, 4).toString());
        npmAdmin.setText(model.getValueAt(index, 5).toString());
        if (model.getValueAt(index, 2).toString().equals("PRIA")){
            priaButtonAdmin.setSelected(true);
            priaButtonAdmin.setSelected(false);
        }else{
            wanitaButtonAdmin.setSelected(true);
            wanitaButtonAdmin.setSelected(false);
        }
        
        Date ttl;
        
        try {
            ttl = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(index, 3).toString());
            ttlAdmin.setDate(ttl);
        } catch (ParseException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_TableDataMouseClicked

    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        String nama = nameAdmin.getText().trim();
        String prodi = prodiAdmin.getText();
        String gender;
        if (priaButtonAdmin.isSelected()){
            gender = "PRIA";
        }else{
            gender = "WANITA";
        }
        String nomor = nomorAdmin.getText().trim();
        String npm = npmAdmin.getText().trim();
        
        if (validateText()){
            SimpleDateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
            String date = dt.format(ttlAdmin.getDate());
            
            Mahasiswa mhs = new Mahasiswa();
            
            mhs.InsertUpdateDelete('i', nama, prodi, null, null, gender, date, npm, nomor, nama);
        }
    }//GEN-LAST:event_AddButtonActionPerformed

